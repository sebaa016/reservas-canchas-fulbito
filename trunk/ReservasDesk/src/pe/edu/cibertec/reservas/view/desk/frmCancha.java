    private void mnGrabarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mnGrabarMouseClicked

        if (intOpcion == NUEVO) {

                GestorCancha.add(new Cancha(0,
                        Integer.parseInt(txtCapacidad.getText()),
                        java.sql.Date.valueOf(txtFechaUltMnat.getText()),
                        txtDescripcion.getText()));


            
        } else {

                GestorCancha.update(new Cancha(Integer.parseInt(txtCodigo.getText()),
                        Integer.parseInt(txtCapacidad.getText()),
                        java.sql.Date.valueOf(txtFechaUltMnat.getText()),
                        txtDescripcion.getText()));

            }

        
        mnGrabar.setEnabled(false);
        mnNuevo.setEnabled(true);
        mnEditar.setEnabled(true);
        refrescarGrid();
        limpiarText();
    }//GEN-LAST:event_mnGrabarMouseClicked

