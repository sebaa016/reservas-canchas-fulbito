    MiTableModel model = new MiTableModel();

    public void refrescarGrid(){
        model.lista = GestorCancha.queryAll();
        tblCanchas.setModel(model);
        model.fireTableChanged(null);
    }
    public void limpiarText(){
        txtCodigo.setText("");
        txtDescripcion.setText("");
        txtFechaUltMnat.setText("");
        txtCapacidad.setText("");

    }

    private void SeleccionarRegistro(){
        int filSel = tblCanchas.getSelectedRow();
        if (filSel != -1){
                intOpcion = MODIFICAR;
                mnEditar.setEnabled(false);
                mnNuevo.setEnabled(false);
                mnGrabar.setEnabled(true);
                mnEliminar.setEnabled(true);
                limpiarText();
                Cancha c = (Cancha) model.lista.get(filSel);
                txtCodigo.setText("" + c.getCodigo());
                txtCapacidad.setText("" + c.getCapacidad());
                txtDescripcion.setText(c.getDescripcion());
                txtFechaUltMnat.setText("" + c.getFechaUltMant());
        }

}

    class MiTableModel extends AbstractTableModel{
        ArrayList lista = new ArrayList();
        String [] titulos = {"Codigo", "Descripcion", "Cap.","Fec.Mant."};

        public int getRowCount() {
            return lista.size();
        }

        public int getColumnCount() {
            return titulos.length;
        }
        public String getColumnName(int column){
            String strTitle = new String();
            if (column < titulos.length){
            strTitle= titulos[column];
            }
            else {
                strTitle ="";
             }
            return strTitle;
        }

        public Object getValueAt(int fil, int col) {
            String valor = "";
                switch(col){
                case 0: valor = "" + ((Cancha)lista.get(fil)).getCodigo(); break;
                case 1: valor = "" + ((Cancha)lista.get(fil)).getCapacidad(); break;
                case 2: valor = "" + ((Cancha)lista.get(fil)).getDescripcion(); break;
                case 3: valor = "" + ((Cancha)lista.get(fil)).getFechaUltMant(); break;


            }
            return valor;
        }

    }


