    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int filSel = tblClientes.getSelectedRow();
        if (filSel != -1){
                limpiarText();
                Cliente c = (Cliente) model.lista.get(filSel);
                txtCodigo.setText("" + c.getCodigo());
                txtDireccion.setText(c.getDireccion());
                txtEmail.setText(c.getEmail());
                txtTelefono.setText("" + c.getTelefono());
                if (c instanceof Persona){
                    rbtnPersona.setSelected(true);
                    rbtnEmpresa.setSelected(false);
                    Persona p = (Persona)c;
                    txtNombre.setText(p.getNombre());
                    txtApellidoPat.setText(p.getApellidoPat());
                    txtApellidoMat.setText(p.getApellidoMat());
                    txtFechaNac.setText(p.getFechaNac().toString());
                    if (p.getSexo() == 'F')
                       cmbSexo.setSelectedIndex(0);
                    else if (p.getSexo() == 'M')
                        cmbSexo.setSelectedIndex(1);
                    txtNroDoc.setText(""+p.getDni());
                } else
                {
                    rbtnPersona.setSelected(false);
                    rbtnEmpresa.setSelected(true);
                    Empresa e = (Empresa)c;
                    txtNombre.setText(e.getRazonSocial());
                    txtNroDoc.setText("" +e.getRuc());
                    txtWeb.setText(e.getPaginaWeb());
                    cmbSexo.setSelectedIndex(-1);
                    txtFechaNac.setText("");
                }
        }

    }//GEN-LAST:event_tblClientesMouseClicked


