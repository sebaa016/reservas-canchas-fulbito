    class MiTableModel extends AbstractTableModel{
        ArrayList lista = new ArrayList();
        String [] titulos = {"Codigo", "Nombre", "Nrodoc"};

        public int getRowCount() {
            return lista.size();
        }

        public int getColumnCount() {
            return titulos.length;
        }
        public String getColumnName(int column){
            String strTitle = new String();
            if (column < titulos.length){
            strTitle= titulos[column];
            }
            else {
                strTitle ="";
             }
            return strTitle;
        }

        public Object getValueAt(int fil, int col) {
            String valor = "";
            Cliente c = (Cliente)lista.get(fil);
            if (c instanceof Persona){
            switch(col){
                case 0: valor = "" + ((Persona)lista.get(fil)).getCodigo(); break;
                case 1: valor = ((Persona)lista.get(fil)).getNombre(); break;
                case 2: valor = "" + ((Persona)lista.get(fil)).getDni(); break;
            }
            }
            else {

                switch(col){
                case 0: valor = "" + ((Empresa)lista.get(fil)).getCodigo(); break;
                case 1: valor = ((Empresa)lista.get(fil)).getRazonSocial(); break;
                case 2: valor = "" + ((Empresa)lista.get(fil)).getRuc(); break;

            }
            }
            return valor;
        }

    }
